SELECT BUNDLE.NAME AS BundleName, SOFTWARE.SOFTWARE_NAME AS PrimarySWName, SOFTWARE_1.SOFTWARE_NAME AS SubSWName, BUNDLE.STATUS AS BundleStatus, substr(char(bundle.record_time),1,10) AS BundleUpdate, BUNDLE.REMOTE_USER AS BundleOperator
FROM (SOFTWARE INNER JOIN (BUNDLE_SOFTWARE INNER JOIN BUNDLE ON BUNDLE_SOFTWARE.BUNDLE_ID=BUNDLE.ID) ON SOFTWARE.SOFTWARE_ID=BUNDLE.SOFTWARE_ID) INNER JOIN SOFTWARE AS SOFTWARE_1 ON BUNDLE_SOFTWARE.SOFTWARE_ID=SOFTWARE_1.SOFTWARE_ID
GROUP BY BUNDLE.NAME, SOFTWARE.SOFTWARE_NAME, SOFTWARE_1.SOFTWARE_NAME, BUNDLE.STATUS, substr(char(bundle.record_time),1,10), BUNDLE.REMOTE_USER
for fetch only 
with ur
;
