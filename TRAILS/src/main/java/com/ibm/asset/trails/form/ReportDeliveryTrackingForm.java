package com.ibm.asset.trails.form;

public class ReportDeliveryTrackingForm {

	private String reportDeliveryCycle;

	private String nextReportDeliveryDate;

	private String lastReportDeliveryDate;

	private String qmxReference;

	public String getReportDeliveryCycle() {
		return reportDeliveryCycle;
	}

	public void setReportDeliveryCycle(String reportDeliveryCycle) {
		this.reportDeliveryCycle = reportDeliveryCycle;
	}

	public String getNextReportDeliveryDate() {
		return nextReportDeliveryDate;
	}

	public void setNextReportDeliveryDate(String nextReportDeliveryDate) {
		this.nextReportDeliveryDate = nextReportDeliveryDate;
	}

	public String getLastReportDeliveryDate() {
		return lastReportDeliveryDate;
	}

	public void setLastReportDeliveryDate(String lastReportDeliveryDate) {
		this.lastReportDeliveryDate = lastReportDeliveryDate;
	}

	public String getQmxReference() {
		return qmxReference;
	}

	public void setQmxReference(String qmxReference) {
		this.qmxReference = qmxReference;
	}

}
